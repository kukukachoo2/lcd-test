uint8_t m = 1;
uint8_t s = 24;

void loop(void) {
  char s_str[2];
  char m_str[2];
  strcpy(s_str, u8x8_u8toa(s, 2));		/* convert s to a string with two digits */
  strcpy(m_str, u8x8_u8toa(m, 2));		/* convert m to a string with two digits */
u8g2.clearBuffer();
u8g2.setFont(u8g2_font_t0_22_tn);
u8g2.drawStr(0,63,m_str);
u8g2.drawStr(33,63,":");
u8g2.drawStr(50,63,s_str);
u8g2.sendBuffer();
  delay(1000);
  s++;
  if ( s == 60 ) {
    s = 0;
    m++;
  }
}


